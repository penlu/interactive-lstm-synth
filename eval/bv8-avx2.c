int bv8_eval(char *prog, uint8_t *res) {
  // check for underflow/too many elements at termination
  int s = 0;
  for (int i = 0; prog[i]; i++) {
    s += inst_argc[prog[i] - '@'];

    if (s <= 0 || s >= STACKSIZE) {
      res[0] = i;
      return -1;
    }
  }
  if (s != 1) {
    res[0] = s;
    return -2;
  }

  // evaluate on all inputs
  // we use AVX-2 GCC intrinsics for this, evaluating in data-parallel
  // on 8 groups of 32 byte values (256 bits) each
  __m256i *locs[8] = {stack, stack + 1, stack + 2, stack + 3,
                      stack + 4, stack + 5, stack + 6, stack + 7};
  for (int v = 0; v < 8; v++) {
    int s = 0;
    __m256i in = vecs[v];
    for (int i = 0; prog[i]; i++) {
      __m256i a, b, c, d, e;
      char *fuck;
      switch (prog[i]) {
        default:
          _mm256_store_si256((stack + v), in);
          fuck = (char*) stack;
          for (int i = 0; i < 16; i++) {
            for (int j = 0; j < 16; j++) {
              printf("%02x ", (uint8_t) fuck[i * 16 + j]);
            }
            printf("\n");
          }
          printf("=================\n");
          break;
      }
      break;
    }
  }
  exit(0);
  return -1000;

  return 0;
}
